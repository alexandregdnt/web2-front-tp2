    public static function validatePhone(string $phone): bool|int
    {
        return preg_match('/^\+?\d{6,16}$/', $phone);
    }

    public static function validatePassword(string $password): bool|int
    {
        return preg_match('/^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[@$!%*?&])[A-Za-z\d@$!%*?&]{8,}$/', $password);
    }

    public static function validateDate(string $date): bool|int
    {
        return preg_match('/^[0-9]{4}-(((0[13578]|(10|12))-(0[1-9]|[1-2][0-9]|3[0-1]))|(02-(0[1-9]|[1-2][0-9]))|((0[469]|11)-(0[1-9]|[1-2][0-9]|30)))$/', $date);
    }

    public static function validateUrl(string $url): bool|int
    {
        return preg_match('/^(https?:\/\/)?([\da-z.-]+)\.([a-z.]{2,6})([\/\w .-]*)*\/?$/', $url);
    }
}